package com.example;

public class Addition {
public static void main(String[] args) {
		
		int a,b,c;
		a=10;
		b=11;
		c=a+b;
		System.out.println("addition is:"+c);
		
		c=a-b;
		System.out.println("Subtraction is:"+c);
		
		c=a*b;
		System.out.println("Multiplication is:"+c);
		
		c=a/b;
		System.out.println("Division is:"+c);
}
}